#  Fazer as operações com variáveis com números atribuídos

a = 10
b = 2

total = a + b  # 12
diferenca = a - b  # 8
multiplicacao = a * b  # 20
divisao = a / b  # 5
exponenciacao = a ** b  # 100
resto_da_divisao = a % b  # 0
divisao_total = a // b  # 5

print(f'a + b = {total}')
print(f'a - b = {diferenca}')
print(f'a * b = {multiplicacao}')
print(f'a / b = {divisao}')
print(f'a ** b = {exponenciacao}')
print(f'a % b = {resto_da_divisao}')
print(f'a // b = {divisao_total}')

