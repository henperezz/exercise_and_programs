print('Subtração, 2-1=', 2-1)  # 1
print('Multiplicação, 3*3=', 3*3)  # 9
print('Divisão, 4/2=', 4/2)  # 2
print('Exponenciação, 2**2=', 2**2)  # 4
print('Resto da divisão, 4/3=', 4 % 3, '4/2=', 4 % 2)  # 1, 0
print('Divisão inteira. 4/3=', 4//3)  # 1
